export const t = (key: string): string => {
  const keys = key.split(".")
  let value: any = translations[currentLang.value]
  for (const k of keys) {
    value = value?.[k]
  }
  return value || key
}

export const changeLanguage = (lang: Language): void => {
  currentLang.value = lang
}
